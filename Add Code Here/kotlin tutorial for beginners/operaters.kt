fun main() {
    var i = 13
    var j = 2

    // maths
    println(i+j)
    println(i-j)
    println(i*j)
    println(i.toFloat() /j)
    println(i%j)
// relational operator
    println(i>j)
    println(i<j)
    println(i>=j)
    println(i<=j)
    println(i==j)
    println(i!=j)

    // increment operator
    var m = 10
//    println(m++) // =  10 // post increment
//    println(m) // =  11
    println(++m) // = 11 // post increment 

}